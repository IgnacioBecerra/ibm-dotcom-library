import { customElement, html, property, LitElement } from 'lit-element';
import settings from 'carbon-components/es/globals/js/settings';
import ddsSettings from '@carbon/ibmdotcom-utilities/es/utilities/settings/settings.js';
import StableSelectorMixin from '../../globals/mixins/stable-selector';

import styles from './cta-section.scss';

// eslint-disable-next-line
const { prefix } = settings;
const { stablePrefix: ddsPrefix } = ddsSettings;

/**
 * The CTA Section pattern
 *
 * @element dds-cta-section
 */
@customElement(`${ddsPrefix}-cta-section`)
class DDSCTASection extends StableSelectorMixin(LitElement) {
  /**
   * Renders CTA heading slot
   */
  protected _renderHeading() {
    const { heading } = this;
    return html`
      <slot name="heading">${heading}</slot>
    `;
  }

  /**
   * Applies section attribute
   */
  connectedCallback() {
    if (!this.hasAttribute('role')) {
      this.setAttribute('role', 'section');
    }
    super.connectedCallback();
  }

  /**
   * The Content Section heading.
   */
  @property()
  heading = '';

  @property()
  copy = '';

  render() {
    return html`
      <dds-content-block>
        <dds-content-block-heading .copy=${this.copy}>${this.heading}</dds-content-block-heading>
        <div class="${ddsPrefix}--content-block__copy"><p>${this.copy}</p></div>
        <dds-button-group>
          <dds-button-group-item href="https://example.com">
            Secondary button
          </dds-button-group-item>

          <dds-button-group-item href="https://example.com">
            Primary button
          </dds-button-group-item>
        </dds-button-group>
      </dds-content-block>
    `;
  }

  /**
   * <section
      data-autoid={`${stablePrefix}--cta-section`}
      className={classNames(`${prefix}--cta-section`, {
        [`${prefix}--cta-section__has-items`]: items,
        [`${prefix}--cta-section--${theme}`]: theme,
      })}
      ref={containerRef}>
      <ContentBlock heading={heading} copy={copy} />
      <CTA customClassName={`${prefix}--cta-section__cta`} {...cta} />
      {items && (
        <div className={`${prefix}--helper-wrapper`}>
          <div className={`${prefix}--content-item-wrapper`}>
            {items.map((item, index) => (
              <ContentItem
                key={index}
                heading={item.heading}
                copy={item.copy}
                cta={item.cta}
              />
            ))}
          </div>
        </div>
      )}
    </section>
   */

  static get stableSelector() {
    return `${ddsPrefix}--cta-section`;
  }

  static styles = styles; // `styles` here is a `CSSResult` generated by custom WebPack loader
}

export default DDSCTASection;
